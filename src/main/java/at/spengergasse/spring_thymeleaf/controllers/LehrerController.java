package at.spengergasse.spring_thymeleaf.controllers;

import at.spengergasse.spring_thymeleaf.entities.Lehrer;
import at.spengergasse.spring_thymeleaf.entities.LehrerRepository;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.*;
import org.springframework.web.servlet.mvc.support.RedirectAttributes;


@Controller
@RequestMapping("/lehrer")
public class LehrerController {
    private final LehrerRepository lehrerRepository;

    public LehrerController(LehrerRepository lehrerRepository) {
        this.lehrerRepository = lehrerRepository;
    }

    @GetMapping("/list")
    public String lehrer(Model model) {
        model.addAttribute("lehrer", lehrerRepository.findAll());
        return "lehrlist";
    }

    @GetMapping("/add") //hinzufügen eines neuen Lehrers
    public String addLehrer(Model model) {
        model.addAttribute("lehrer", new Lehrer());
        return "add_lehrer";
    }

    @PostMapping("/add") //speichern eines neuen Lehrers
    public String addLehrer(@ModelAttribute("lehrer") Lehrer lehrer) {
        lehrerRepository.save(lehrer);
        return "redirect:/lehrer/list";
    }

    @GetMapping("/edit/{id}") //bearbeiten eines Lehrers
    public String editLehrer(Model model, @PathVariable int id) {
        Lehrer lehrer = lehrerRepository.findById(id)
                .orElseThrow(() -> new RuntimeException("Lehrer nicht gefunden"));
        model.addAttribute("lehrer", lehrer);
        return "edit_lehrer";
    }

    @PostMapping("/edit/{id}") //aktualisieren eines Lehrers
    public String editLehrer(@PathVariable int id, @ModelAttribute("lehrer") Lehrer lehrer) {
        lehrer.setId(id);
        lehrerRepository.save(lehrer);
        return "redirect:/lehrer/list";
    }

    @PostMapping("/delete/{id}") //löschen eines Lehrers
    public String deleteLehrer(@PathVariable Integer id) {
        lehrerRepository.deleteById(id);
        return "redirect:/lehrer/list";
    }

}













































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































